serieTuple= ("Brooklyn 99", "The Oficce", "Tudo Bem não Ser Normal", "Suits", "Jovem Shelton")
print(type(serieTuple))

# 1 - Buscar os dois primeiros itens da tupla
print(serieTuple[:2])

# 2 - Buscar o último item da tupla 
print(serieTuple[-1])

# 3- Buscar séries até uma determinada posição
print(serieTuple[:3])

# 4 - Buscar séries de uma posição em diante
print(serieTuple[2:])

# 5 - Recuperar um item da tupla pelo nome
print(serieTuple.index("The Oficce"))